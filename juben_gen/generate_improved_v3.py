# 对话行的判断：不以 ▲/【/人物 开头，不是场次/集数标题，且包含中文冒号
_DIALOGUE_RE = re.compile(
    r"^(?!▲)(?!【)(?!人物：)(?!\d+-\d+场)(?!第\d+集)"  # 排除非对话行
    r".+：",                                              # 角色名+冒号
)


def _is_dialogue(line: str) -> bool:
    return bool(_DIALOGUE_RE.match(line.strip()))


